document.getElementById("1").addEventListener('click', () => {
    $("#a1").slideToggle(350);
    if (document.getElementById("1").innerHTML == `<i class="fa fa-angle-down"></i>`)
        document.getElementById("1").innerHTML = `<i class="fa fa-angle-up"></i>`;
    else
        document.getElementById("1").innerHTML = `<i class="fa fa-angle-down"></i>`
})

